import Header from "@/components/common/Header";
import { type Metadata } from "next";
import { Prompt } from "next/font/google";
import Providers from "./providers";

import "@/styles/globals.css";

// 🎨 Theme-aware font classes
const getThemeClasses = () => {
  // ใน server-side เราต้องใช้ static classes
  // Theme จะถูกจัดการโดย ThemeProvider ใน client-side
  return "light"; // Default theme
};

// 🎨 กำหนด Google Font Prompt - Optimized
const promptFont = Prompt({
  subsets: ["latin", "thai"],
  weight: ["300", "400", "600"], // Reduced from 9 weights (100-900) to 3 essential weights
  style: ["normal"], // Removed italic variants (rarely used in Thai)
  display: "swap",
  variable: "--font-prompt",
  preload: true, // Preload for faster initial render
  adjustFontFallback: false, // Reduce font metrics overhead
});

// ใช้ static font class names เพื่อป้องกัน hydration mismatch
const FONT_CLASSES = `${promptFont.variable} ${promptFont.className} antialiased`;

export const metadata: Metadata = {
  title: "Bun LINE T3 App",
  description: "Generated by create-t3-app",
  icons: [
    { rel: "icon", url: "/favicon.ico" },
    {
      rel: "icon",
      url: "data:image/svg+xml,<svg xmlns=%22http://www.w3.org/2000/svg%22 viewBox=%220 0 100 100%22><text y=%22.9em%22 font-size=%2290%22>🦦</text></svg>",
    },
  ],
};

export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const themeClass = getThemeClasses();
  return (
    <html
      id="html-root"
      lang="th"
      className={`${promptFont.variable} ${promptFont.className} ${themeClass}`}
      suppressHydrationWarning={true}
    >
      <body
        id="body-main"
        className={`${FONT_CLASSES} min-h-screen bg-background text-foreground transition-colors duration-300`}
        suppressHydrationWarning={true}
      >
        <Providers>
          <div id="modal-root"></div>
          <Header />
          <main id="main-content">{children}</main>
        </Providers>
      </body>
    </html>
  );
}
